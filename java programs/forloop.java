package revision;

public class forloop {
    public static void main(String[] args) {
        for (int i = 10; i <= 15; i++)
        {
            for (int j=10 ; j<=i;j++)
            {
                System.out.print(i);
            }
            System.out.println();
        }


//        array
        int b[]={12,13,16,19,20,11};
        for(int i :b)
        {
            System.out.println(i);
        }


    }
}
